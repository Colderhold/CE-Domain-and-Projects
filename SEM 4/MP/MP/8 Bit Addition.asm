DATA SEGMENT
   A DB 0FH
   B DB 06H
   SUM DB ?
   CARRY DB ?
DATA ENDS

CODE SEGMENT
    ASSUME CS:CODE,DS:DATA
    
    START:MOV AX,DATA
    MOV DS,AX 
    MOV AL,A
    ADD AL,B
    JNC SKIP
    INC CARRY
    SKIP:MOV SUM,AL
    MOV AX,4CH
    INT 21H
   
CODE ENDS
END START 