DATA SEGMENT
   A DB 0FH
   B DB 06H
   DIF DB ?
   BORROW DB ?
DATA ENDS

CODE SEGMENT
    ASSUME CS:CODE,DS:DATA
    
    START:MOV AX,DATA
    MOV DS,AX 
    MOV AL,A
    SUB AL,B
    JNC SKIP
    INC BORROW
    SKIP:MOV DIF,AL
    MOV AX,4CH
    INT 21H
   
CODE ENDS
END START 